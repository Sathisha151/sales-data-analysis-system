       private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tbluser.getModel();
    model.setRowCount(0); // Clear the table before populating new data

    // Use the correct column name "Datel" in the query
    String query = "SELECT TransactionID, CustomerID, ProductID, ProductName, Quantity, PricePerUnit, Date, TotalPrice, Region FROM Sales";
    try (Connection conn = database.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(query)) {

        while (rs.next()) {
            // Add each row to the table
            model.addRow(new Object[]{
                rs.getInt("TransactionID"),
                rs.getInt("CustomerID"),
                rs.getInt("ProductID"),
                rs.getString("ProductName"),
                rs.getInt("Quantity"),
                rs.getDouble("PricePerUnit"),
                rs.getString("Date"), // Use "Datel" here
                rs.getDouble("TotalPrice"),
                rs.getString("Region")
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error fetching data: " + e.getMessage());
    }
}
    


