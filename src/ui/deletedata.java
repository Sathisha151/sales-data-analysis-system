    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String transactionID = txttransactionID.getText().trim();

    // Check if Transaction ID is empty
    if (transactionID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a Transaction ID!");
        return;
    }

    String query = "DELETE FROM Sales WHERE TransactionID = ?";
    try (Connection conn = database.getConnection();
         PreparedStatement stmt = conn.prepareStatement(query)) {

        // Parse Transaction ID to integer
        stmt.setInt(1, Integer.parseInt(transactionID));

        // Execute the delete query
        int rowsDeleted = stmt.executeUpdate();

        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Data Deleted Successfully!");
            // Refresh the table after deletion
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "No data found with the given Transaction ID!");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid Transaction ID! Please enter a valid number.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error deleting data: " + e.getMessage());
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

